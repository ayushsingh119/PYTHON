import math
pi = 3.14
print(round(pi))  # it will round off the number to 3
print(math.ceil(pi))  # it will round off the number  to the upper of the value
print(math.floor(pi))   # it will round off the number to the lowest of the value
print(abs(pi))  # it will give you the absolute value  which means how far is the value from 0 in positve of negative integer means it will give the same value as the value is .
print(pow(pi,2))  # it will give you the power of the value
print(math.sqrt(pi))  # it will give you the square root of the value


