# typecasting: convert the datatype of a value to another data type

x = 1  # int
y = 2.0  # float
z = "3"  # str

print(x)
print(int(y))  # it will only help to print the int value of float
print(int(z)*3)

print("x is: " + str(x))  # here we have to convert int value into str because we can only concatinate string with string so we change datatype of int to str.
